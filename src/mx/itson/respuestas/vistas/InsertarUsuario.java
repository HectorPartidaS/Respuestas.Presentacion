    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Usuario u = new Usuario();
        String name = tbNombre.getText();
        String user = tbCuenta.getText();
        String contra = tbPassword.getText();
        String date =tbFecha.getText();
        try {
            Date fecha = new SimpleDateFormat("yyyy-MM-dd").parse(date);
            u.guardar(name, user, contra, fecha);
        } catch (ParseException ex) {
            Logger.getLogger(InsertarUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

